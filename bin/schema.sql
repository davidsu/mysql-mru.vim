CREATE DATABASE IF NOT EXISTS mru_vim;
use mru_vim;
CREATE TABLE IF NOT EXISTS mru(
    _file VARCHAR(255) NOT NULL,
    linenum INT NOT NULL DEFAULT 1,
    ts TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    PRIMARY KEY (_file)
);


CREATE TABLE IF NOT EXISTS mrw(
    _file VARCHAR(255) NOT NULL,
    linenum INT NOT NULL DEFAULT 1,
    ts TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    PRIMARY KEY (_file)
);

